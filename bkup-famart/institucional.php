								<p>
									<h1 class="titulo-h1">Regime Acadêmico</h1>
									O processo de avaliação está disciplinado no Regimento da Faculdade FAMART, no TÍTULO V – DO REGIME ACADÊMICO, CAPÍTULO VI, envolvendo normas sobre a Avaliação do Rendimento Acadêmico.<br><br>

									<strong>Capítulo VI – Da Avaliação do Rendimento Acadêmico</strong>

									Art. 68. A avaliação do rendimento acadêmico é feita por disciplina, incidindo sobre a frequência e o aproveitamento acadêmico do aluno.<br><br>

									Art. 69. A frequência às aulas e demais atividades acadêmicas, permitida apenas aos matriculados, é obrigatória aos alunos, vedado o abono de faltas, ressalvados os casos previstos na legislação vigente.<br>
									§1º. Independentemente dos demais resultados obtidos, é considerado reprovado na disciplina o aluno que não obtenha frequência em, no mínimo, 75% (setenta e cinco por cento) das aulas e demais atividades programadas.<br>
									§2º. A verificação e registro de frequência são da responsabilidade do professor, e seu controle, para efeito do parágrafo anterior, da Secretaria. <br><br>


									Art. 70. O aproveitamento acadêmico é avaliado através de acompanhamento contínuo do aluno e dos resultados por ele obtidos nos exercícios acadêmicos e no exame final, sempre escritos, exceto no caso do inciso I do artigo 67.<br>
									§1º. Compete ao professor da disciplina elaborar os exercícios acadêmicos, sob a forma de prova, e determinar os demais trabalhos, bem como julgar os resultados.<br>
									§2º. Os exercícios acadêmicos, em número de 02 (dois) por período letivo, constam de trabalhos de avaliação e outras formas de verificação previstas no plano de ensino da disciplina.<br><br>

									Art. 71. A cada verificação de aproveitamento é atribuída uma nota, expressa em grau numérico de 0 (zero) a 10 (dez).
									Parágrafo Único. Ressalvado o disposto no artigo 69, atribui-se nota 0 (zero) ao aluno que deixar de se submeter à verificação prevista na data fixada, bem como ao que nela se utilizar de meio fraudulento.<br><br>

									Art. 72. A nota final do aluno em cada disciplina, verificada ao término do período letivo, será a média aritmética simples entre as notas de verificação de aproveitamento e a nota do exame final.<br><br>

									Art. 73. É concedida prova substitutiva ao aluno que deixar de realizar prova de aproveitamento acadêmico no período estabelecido no calendário acadêmico.<br>
									§1º. A prova substitutiva é realizada mediante requerimento do aluno no prazo estabelecido pela Secretaria.<br>
									§2º. Conceder-se-á segunda chamada ao aluno que faltar ao exame final, desde que requerida no prazo improrrogável de 08 (oito) dias que se seguirem à sua realização, uma vez justificada a ausência e a juízo do Diretor Geral.<br><br>

									Art. 74. Atendida em qualquer caso a frequência mínima de 75% (setenta e cinco por cento) às aulas e demais atividades acadêmicas, é aprovado:<br>
									I – independentemente do exame final, o aluno que obtiver nota de aproveitamento não inferior a 7,0 (sete), correspondentemente à média aritmética, sem arredondamento, das notas dos exercícios acadêmicos;<br>
									II – mediante exame final o aluno que, tendo obtido nota de aproveitamento inferior a 7,0 (sete), porém não inferior a 3,0 (três), obtiver nota final não inferior a 5,0 (cinco) correspondente à média aritmética, sem arredondamento, entre a nota de aproveitamento e a nota de exame final.<br><br>

									Art. 75. O aluno reprovado por não ter alcançado seja a frequência, sejam as notas mínimas exigidas, repetirá a disciplina, sujeito, na repetência, às mesmas exigências de frequência e de aproveitamento estabelecidas neste Regimento Geral.<br><br>

									Art. 76. É promovido ao semestre seguinte o aluno aprovado em todas as disciplinas do semestre letivo cursado, admitindo-se ainda a promoção com dependência em até 02 (duas) disciplinas deste semestre.<br>
									Parágrafo Único. O aluno reprovado em mais de 02 (duas) disciplinas repetirá o período, ficando, porém dispensado das disciplinas em que obteve aprovação.
